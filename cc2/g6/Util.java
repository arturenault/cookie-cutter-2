    public static int findArgMin(int start, int end, Function<Integer, Integer> f) {
        int idx = start;
        int minval = Integer.MAX_VALUE;
        for (int l = start; l < end; ++l) {
            int test = f.apply(l);
            if (test < minval) {
                minval = test;
                idx = l;
            }
        }
        return idx;
    }

    /**
     * Returns (for this rotation of the shape)
     * an array containing:
     * 0: width of the
     *
     * @param s
     * @return
     */
    public static Point[] dimensions(Shape s) {
        int left = Integer.MAX_VALUE;
        int right = Integer.MIN_VALUE;
        int top = Integer.MIN_VALUE;
        int bottom = Integer.MAX_VALUE;

        for (Point p : s) {
            left = Integer.min(p.i, left);
            right = Integer.max(p.i, right);
            bottom = Integer.min(p.j, left);
            top = Integer.max(p.j, right);
        }

        Point out[] = new Point[3];
        out[0] = new Point(right - left, top - bottom);
        out[1] = new Point(left, bottom);
        out[2] = new Point(right, top);

        return out;
    }

    public static int findMin(int start, int end, Function<Integer, Integer> f) {
        int i = findArgMin(start, end, f);
        return f.apply(i);
    }

    public static List<Move> getValidMoves(Dough d, Shape c, int si) {
        // find all valid cuts

        List<Move> moves = new ArrayList<>();
        if (c == null) {
            return moves;
        }

        Shape[] rotations = c.rotations();
        for (int i = 0; i < d.side(); ++i) {
            for (int ri = 0; ri < rotations.length; ++ri) {
                Shape s = rotations[ri];
                for (int j = 0; j < d.side(); ++j) {
                    Point p = new Point(i, j);
                    if (d.cuts(s, p)) {
                        moves.add(new Move(si, ri, p));
                    }
                }
            }
        }
        return moves;
    }

    public static List<Move> getValidMoves(Dough d, Shape C[], int si_arr[]) {
        // find all valid cuts

        List<Move> moves = new ArrayList<>();

        for (int si = 0; si < C.length; ++si) {
            Shape[] rotations = C[si].rotations();
            for (int i = 0; i < d.side(); ++i) {
                for (int ri = 0; ri < rotations.length; ++ri) {
                    Shape s = rotations[ri];
                    for (int j = 0; j < d.side(); ++j) {
                        Point p = new Point(i, j);
                        if (d.cuts(s, p)) {
                            moves.add(new Move(si_arr[si], ri, p));
                        }
                    }
                }
            }
        }

        return moves;
    }
    public static int evaluateTilingShape(Shape opp, Shape s, int sidelen) {
        Dough d = new Dough(sidelen);
        boolean play_opp = true;

        int plays = 0;
        do {
            List<Move> opp_moves = getValidMoves(d, opp, 0);
            if (opp_moves.isEmpty()) {
                play_opp = false;
            } else {
                Move m = opp_moves.get(0);
                d.cut(opp.rotations()[m.rotation], m.point);
            }
        } while (play_opp);

        while (true) {

            List<Move> s_moves = getValidMoves(d, s, 0);

            if (s_moves.isEmpty()) {
                break;
            }

            Move m = s_moves.get(0);
            d.cut(s.rotations()[m.rotation], m.point);

            ++plays;
        }

        return plays;
    }

    public static Shape getOctomino(int idx) {
        int coord_list[] = ALL_OCTOMINOES[idx];

        Point p[] = new Point[8];

        for (int i = 0; i < 8; ++i) {
            p[i] = new Point(coord_list[2 * i], coord_list[2 * i + 1]);
        }

        return new Shape(p);
    }

    public static Shape getPentomino(int idx) {
        int coord_list[] = ALL_PENTOMINOES[idx];

        Point p[] = new Point[5];

        for (int i = 0; i < 5; ++i) {
            p[i] = new Point(coord_list[2 * i], coord_list[2 * i + 1]);
        }

        return new Shape(p);
    }


